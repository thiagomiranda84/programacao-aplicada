#PARA UMA VARIAVEL C DENTRO DE UMA LISTA DE VALORES
  # ENQUANTO HOUVER VALORES EM UMA LISTA PASSE UM VALOR DE CADA VEZ PARA A VARIVEL DE CONTROLE
