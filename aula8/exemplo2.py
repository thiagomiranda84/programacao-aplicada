#range -> Gera uma lista com um intervalo de valores.
#range(1valor) -> valor representa a quantidade de itens gerados sempre iniciando do 0
#range(2valores) -> Valor 1 representa o inicio da lista e o valor 2 representa onde a lista finaliza, sem inclui-lo.
#range(3valores) -> valor1 representa o inicio da lista, o valor 2 representa o fim da lista, sem estar incluido e valor 3 representa o passo (o quanto a variavel de controle será modificada.
for i in range(1,11,2):
    print(i)












