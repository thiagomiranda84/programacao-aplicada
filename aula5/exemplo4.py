idade = int( input("Digite a sua idade: ") )
#Se a idade for maior ou igual a 18 imprima a mensagem
if idade >= 18:
    print(f"A pessoa tem {idade} anos e ela é obrigada a votar")
else: #Se a idade NÃO FOR maior ou igual a 18 imprima a mensagem abaixo
    print(f"A pessoa tem {idade} anos e não é obrigada a votar")

